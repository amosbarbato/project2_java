package main.project.main;

public class Client {
    private String Name;
    private Long CodeReg;
    private Long Phone;
    private String Adress;
    private Integer NumberAdress;
    private String City;
    private String Uf;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Long getCodeReg() {
        return CodeReg;
    }

    public void setCodeReg(Long CodeReg) {
        CodeReg = CodeReg;
    }

    public Long getPhone() {
        return Phone;
    }

    public void setPhone(Long phone) {
        Phone = phone;
    }

    public String getAdress() {
        return Adress;
    }

    public void setAdress(String adress) {
        Adress = adress;
    }

    public Integer getNumberAdress() {
        return NumberAdress;
    }

    public void setNumberAdress(Integer numberAdress) {
        NumberAdress = numberAdress;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

    public String getUf() {
        return Uf;
    }

    public void setUf(String uf) {
        Uf = uf;
    }
}
